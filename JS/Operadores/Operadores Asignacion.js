// Que son los Operadores De Asignacion?

/*Un operador de asignacion asigna un valor al operando de la izquierda basado en el valor del operando de la derecha*/

//Suma:
//Para sumar debemos poner "+="" y poner el numero, ejemplo:

let numeroSuma = 10;
//Aclaramos la variable

numeroSuma += 5;
//Luego ala variable le ponemos que nos sume con "+="

document.write(numeroSuma)

//Resta:
//Para restar es casi lo mismo solo que ponemos el signo -=, ejemplo:

let numeroResta = 2;
//Aclaramos la variable

numeroResta -= 1

document.write(numeroResta)

//Multiplicacion:
//Para Multiplicar es casi lo mismo solo que ponemos el signo *=, ejemplo:

let numeroMultiplicacion = 5;
//Aclaramos la variable

numeroMultiplicacion *= 2

document.write(numeroMultiplicacion)

//Division:
//Para Dividir es casi lo mismo solo que ponemos el signo de /=, ejemplo:

let numeroDivision = 10;
//Aclaramos la variable

numeroDivision /= 2

document.write(numeroDivision)

//Asignacion de resto:
//Esto esta mas complicado, por que esto lo que hace divide, y nos dice cual es el resto, por ejemplo:

let numeroADR = 50;
//Aclaramos la variable

numeroADR %= 6
//Ponemos 6 para saber cuanto es el resto

document.write(numeroADR)

//Potencias
//Esto es para las potencias, ejemplo:

let numeroPotencias = 5;
//Aclaramos la variable

numeroPotencias **= 2

document.write(numeroPotencias)



