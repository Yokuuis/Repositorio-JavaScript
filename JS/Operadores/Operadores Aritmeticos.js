// Que son los Operadores Aritmeticos?

/* Los Operadores Aritmeticos toman valores numericos (ya sean literales o variables) como sus operandos y 
retornan un valor numerico unico*/

//Ejemplo de suma:

numberaddition = 10;
//Aclaramos el valor de number1

numberaddition2 = 5;
//Aclaramos el segundo valor para hacer la suma
resultado = numberaddition + numberaddition2;
//Aclaramos resultado para hacer lo que vaya hacer
alert(resultado)
//Que nos muestre el resultado


//Ejemplo de Resta:

numberRest = 10;

numberRest--;

resultado = numberRest;

alert(resultado)

//Ejemplo de Division:

numberDivide = 10;

numberDivide2 = 2;

resultado = numberDivide / numberDivide2;

alert(resultado)

//Ejemplo de Potenciales:

NumberPotential = 5;

NumberPotencial2 = 2;

resultado = NumberPotential**NumberPotencial2;

alert(resultado)

//Ejemplo de Aumentado:

numberIncrease = 5;

numberIncrease++;

resultado = numberIncrease;

alert(resultado)

//Ejemplo Remainder:

numberRemainder = 10;

numberRemainder2 = 2;

resultado = numberRemainder&numberRemainder2;

//Ejemplo Subtraccion (resta normal):

numberSubtraction = 2;

numberSubtraction1 = 1;

resultado = numberSubtraction-numberSubtraction1;

alert(resultado)

//Negacion Unuaria

numberUnary = 10;

resultado = -numberUnary;

alert(resultado)